/**
 * Debugging Techniques
 * 
 * WHAT TO LEARN:
 * 
 * 1. CONSOLE.LOG
 *    - console.log(value)
 *    - Basic logging
 *    - Multiple values
 *    - console.log(a, b, c)
 * 
 * 2. CONSOLE METHODS
 *    - console.error() - errors (red)
 *    - console.warn() - warnings (yellow)
 *    - console.info() - info
 *    - console.debug() - debug
 *    - console.table() - table view
 * 
 * 3. CONSOLE.DIR
 *    - console.dir(obj)
 *    - Object inspection
 *    - Expandable properties
 *    - Better for objects
 * 
 * 4. CONSOLE.TRACE
 *    - console.trace()
 *    - Stack trace
 *    - Shows call stack
 *    - Useful for debugging flow
 * 
 * 5. CONSOLE.ASSERT
 *    - console.assert(condition, message)
 *    - Logs if condition false
 *    - Silent if true
 * 
 * 6. DEBUGGER STATEMENT
 *    - debugger;
 *    - Pauses execution
 *    - Opens DevTools
 *    - Breakpoint in code
 * 
 * 7. BROWSER DEVTOOLS
 *    - F12 or right-click â†’ Inspect
 *    - Sources tab for debugging
 *    - Console tab for output
 *    - Network tab for requests
 * 
 * 8. BREAKPOINTS
 *    - Click line number in Sources
 *    - Pauses execution
 *    - Inspect variables
 *    - Step through code
 * 
 * 9. STEP CONTROLS
 *    - Step over (F10) - next line
 *    - Step into (F11) - into function
 *    - Step out (Shift+F11) - out of function
 *    - Resume (F8) - continue
 * 
 * 10. WATCH EXPRESSIONS
 *     - Add variables to watch
 *     - See values update
 *     - Useful for debugging
 * 
 * 11. CALL STACK
 *     - Shows function calls
 *     - Navigate between frames
 *     - See execution path
 * 
 * 12. SCOPE PANEL
 *     - See all variables
 *     - Local, closure, global
 *     - Current values
 * 
 * 13. CONSOLE.TIME
 *     - console.time('label')
 *     - console.timeEnd('label')
 *     - Measures execution time
 *     - Performance profiling
 * 
 * 14. PERFORMANCE TAB
 *     - Record performance
 *     - Find bottlenecks
 *     - Analyze runtime
 * 
 * 15. NETWORK TAB
 *     - See HTTP requests
 *     - Response times
 *     - Request/response data
 *     - Failed requests
 * 
 * 16. APPLICATION TAB
 *     - LocalStorage
 *     - SessionStorage
 *     - Cookies
 *     - Inspect storage
 * 
 * 17. SOURCE MAPS
 *     - Debug minified code
 *     - Original source code
 *     - Better debugging
 * 
 * 18. REMOTE DEBUGGING
 *     - Chrome DevTools for mobile
 *     - Debug mobile browsers
 *     - USB debugging
 */

